<script>
  // 스크롤 이벤트 리스너 추가
  $(window).on('scroll', function () {
    // 그래프가 있는 요소의 위치 확인
    var graphPosition = $('.dys-overview-graph--male').offset().top;

    // 현재 스크롤 위치 확인
    var scrollPosition = $(window).scrollTop() + $(window).height();

    // 그래프가 보이는 부분까지 스크롤했을 때 스크립트 실행
    if (scrollPosition > graphPosition) {
      draw(15, '.dys-overview-graph--male .dys-overview-percentage', '#52A2C6');
      draw(3, '.dys-overview-graph--female .dys-overview-percentage', '#E5685C');

      // 스크롤 이벤트 제거 (스크립트가 한 번만 실행되도록)
      $(window).off('scroll');
    }
  });

  function draw(max, classname, colorname) {
    var i = 1;
    var func1 = setInterval(function () {
      if (i < max) {
        color1(i, classname, colorname);
        i++;
      } else {
        clearInterval(func1);
      }
    }, 40);
  }

  function color1(i, classname, colorname) {
    $(classname).css({
      "background": "conic-gradient(" + colorname + " 0% " + (i - 0.1) + "%, #f0f0f0 " + i + "% 100%)"
    });
  }
</script>